$('.idul').mouseover(function(){
	$('.idul').css({'box-shadow':' 0px 1px 2px 1px rgb(0 0 0 / 0.1)'});
	$('.idyan').css({'background':'#fff','color':'#b0b0b0'});
	$('.yan').css({'background':'#fff','color':'#b0b0b0'});
	$('.ran').css({'background':'#fff','color':'#b0b0b0'});
	$('.idyan').show();
	$('.yan').show();
	$('.ran').show();
}).mouseout(function(){
	$('.idul').css({'box-shadow':' 0px 0px 0px 0px rgb(0 0 0 / 0)'});
	$('.idyan').css({'background':'#333333','color':'#b0b0b0'});
	$('.yan').hide();
	$('.ran').hide();
})

